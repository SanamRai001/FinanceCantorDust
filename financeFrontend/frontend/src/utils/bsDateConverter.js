// BS month names
export const BS_MONTH_NAMES = [
  'Baisakh', 'Jestha', 'Ashadh', 'Shrawan',
  'Bhadra',  'Ashwin', 'Kartik', 'Mangsir',
  'Poush',   'Magh',   'Falgun', 'Chaitra'
];

// AD start date that maps to BS 2000-01-01
const AD_START = { year: 1943, month: 4, day: 14 }; // April 14 1943

// ── AD to BS conversion ───────────────────
export const adToBS = (adDate) => {
  const date = adDate instanceof Date ? adDate : new Date(adDate);

  if (isNaN(date)) return null;

  let adYear  = date.getFullYear();
  let adMonth = date.getMonth() + 1; // JS months are 0-indexed
  let adDay   = date.getDate();

  // calculate total days from AD start date
  const startDate = new Date(AD_START.year, AD_START.month - 1, AD_START.day);
  const diffTime  = date - startDate;
  let   totalDays = Math.floor(diffTime / (1000 * 60 * 60 * 24));

  if (totalDays < 0) return null; // date before BS 2000

  // find BS year, month, day
  let bsYear  = 2000;
  let bsMonth = 1;
  let bsDay   = 1;

  // subtract days year by year
  while (totalDays > 0) {
    const monthDays = BS_MONTHS[bsYear];
    if (!monthDays) break;

    const daysInYear = monthDays.reduce((a, b) => a + b, 0);

    if (totalDays >= daysInYear) {
      totalDays -= daysInYear;
      bsYear++;
    } else {
      // subtract days month by month
      for (let m = 0; m < 12; m++) {
        if (totalDays >= monthDays[m]) {
          totalDays -= monthDays[m];
          bsMonth = m + 2;
        } else {
          bsDay   = totalDays + 1;
          bsMonth = m + 1;
          totalDays = 0;
          break;
        }
      }
    }
  }

  return { year: bsYear, month: bsMonth, day: bsDay };
};

// ── Format BS date as string ──────────────
// returns "2081-04-15" by default
// pass format='name' to get "15 Baisakh 2081"
export const formatBS = (adDate, format = 'numeric') => {
  const bs = adToBS(adDate);
  if (!bs) return '—';

  const y = bs.year;
  const m = String(bs.month).padStart(2, '0');
  const d = String(bs.day).padStart(2, '0');

  if (format === 'name') {
    return `${bs.day} ${BS_MONTH_NAMES[bs.month - 1]} ${bs.year}`;
  }

  return `${y}-${m}-${d}`;
};

// ── Get current BS date ───────────────────
export const todayBS = () => formatBS(new Date());

// ── Get BS fiscal year ────────────────────
// Nepal fiscal year starts Shrawan (month 4)
// returns "2081-82" format
export const getFiscalYear = (adDate) => {
  const bs = adToBS(adDate || new Date());
  if (!bs) return '—';

  if (bs.month >= 4) {
    return `${bs.year}-${String(bs.year + 1).slice(2)}`;
  } else {
    return `${bs.year - 1}-${String(bs.year).slice(2)}`;
  }
};